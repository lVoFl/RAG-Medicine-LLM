DRUGS = ["氨氯地平", "缬沙坦", "厄贝沙坦", "培哚普利", "非洛地平"]
AGES = [32, 38, 45, 52, 58, 63, 67, 72]
BPS = ["138/88", "145/92", "152/96", "160/100", "168/102"]
FBG = ["7.2", "7.8", "8.4", "9.1", "10.3"]
HBA1C = ["7.1", "7.8", "8.4", "9.0", "9.8"]
LDL = ["2.8", "3.2", "3.8", "4.3", "4.9"]


def fill_template(text: str, rnd: random.Random) -> str:
    return (
        text.replace("{age}", str(rnd.choice(AGES)))
        .replace("{bp}", rnd.choice(BPS))
        .replace("{drug}", rnd.choice(DRUGS))
        .replace("{fbg}", rnd.choice(FBG))
        .replace("{hba1c}", rnd.choice(HBA1C))
        .replace("{ldl}", rnd.choice(LDL))
    )


def generate_questions(target_count: int, seed: int) -> list[dict]:
    rnd = random.Random(seed)
    all_items = []
    seen = set()
    for topic, templates in TOPIC_TEMPLATES.items():
        for template in templates:
            q = fill_template(template, rnd).strip()
            if q in seen:
                continue
            seen.add(q)
            all_items.append(
                {
                    "source": "synthetic_hq_question_v2",
                    "topic": topic,
                    "difficulty": rnd.choice(["中", "中高", "高"]),
                    "question": q,
                }
            )

    rnd.shuffle(all_items)
    if target_count <= len(all_items):
        return all_items[:target_count]

    # If target_count exceeds unique pool size, cycle with slight suffix variation.
    out = list(all_items)
    idx = 0
    while len(out) < target_count:
        base = dict(all_items[idx % len(all_items)])
        idx += 1
        base["question"] = base["question"] + "（请结合个体风险分层说明）"
        if base["question"] in seen:
            continue
        seen.add(base["question"])
        out.append(base)
    return out


def main() -> None:
    parser = argparse.ArgumentParser(description="Generate high-quality three-high questions.")
    parser.add_argument(
        "--output",
        type=str,
        default="SFT_data/three_high_subset/high_quality_questions.jsonl",
        help="Output jsonl file path (relative to model dir).",
    )
    parser.add_argument("--count", type=int, default=240, help="Number of questions to generate.")
    parser.add_argument("--seed", type=int, default=42)
    args = parser.parse_args()

    output_path = Path(args.output)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    questions = generate_questions(target_count=args.count, seed=args.seed)
    with output_path.open("w", encoding="utf-8") as f:
        for item in questions:
            f.write(json.dumps(item, ensure_ascii=False) + "\n")

    print(f"Generated {len(questions)} questions -> {output_path}")


if __name__ == "__main__":
    main()
